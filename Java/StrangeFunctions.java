package Codeforces.Sep17;

import java.util.Scanner;

public class StrangeFunctions {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int n=sc.nextInt();
        for(int i=0;i<n;i++){
            String  x=sc.next();
            System.out.println(x.length());
        }
    }
}
